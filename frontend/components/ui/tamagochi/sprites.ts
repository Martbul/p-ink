/**
 * Merge a base sprite frame with outfit + accessory overlays.
 * Overlay pixel value 0 = transparent (keep base pixel).
 */
export function mergeOverlays(
  base: Frame,
  outfitId: string,
  accessoryId: string,
): Frame {
  const outfit = OUTFIT_OVERLAYS[outfitId];
  const acc = ACCESSORY_OVERLAYS[accessoryId];
  if ((!outfit || outfit.length === 0) && (!acc || acc.length === 0))
    return base;

  const h = base.length;
  const w = base[0]?.length ?? 0;
  const result: Frame = base.map((row) => [...row]);

  if (outfit && outfit.length > 0) {
    for (let y = 0; y < Math.min(h, outfit.length); y++) {
      for (let x = 0; x < Math.min(w, (outfit[y] ?? []).length); x++) {
        if (outfit[y][x] !== 0) result[y][x] = outfit[y][x];
      }
    }
  }

  if (acc && acc.length > 0) {
    for (let y = 0; y < Math.min(h, acc.length); y++) {
      for (let x = 0; x < Math.min(w, (acc[y] ?? []).length); x++) {
        if (acc[y][x] !== 0) result[y][x] = acc[y][x];
      }
    }
  }

  return result;
}
