interface PixelBgProps {
  type: string;
  scale?: number;
  fillParent?: boolean;
  style?: React.CSSProperties;
}

export function PixelBg({ type, scale = 3, fillParent = false, style = {} }: PixelBgProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const bg = BACKGROUNDS[type] ?? BACKGROUNDS.night_city;

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    ctx.imageSmoothingEnabled = false;
    const h = bg.pixels.length;
    const w = bg.pixels[0]?.length ?? 0;
    canvas.width = w * scale;
    canvas.height = h * scale;
    for (let y = 0; y < h; y++) {
      for (let x = 0; x < w; x++) {
        const key = bg.pixels[y][x];
        const color = bg.palette[key];
        if (color) {
          ctx.fillStyle = color;
          ctx.fillRect(x * scale, y * scale, scale, scale);
        }
      }
    }
  }, [type, scale, bg]);

  if (fillParent) {
    return (
      <canvas
        ref={canvasRef}
        style={{
          imageRendering: "pixelated",
          width: "100%",
          height: "100%",
          objectFit: "cover",
          display: "block",
          ...style,
        }}
      />
    );
  }

  return <canvas ref={canvasRef} style={{ imageRendering: "pixelated", display: "block", ...style }} />;
}