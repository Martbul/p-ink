type Palette = Record<number, string>;
type Frame = number[][];

interface BgDef {
  palette: Record<string, string>;
  pixels: string[][];
}

// ═══════════════════════════════════════════════════════════════
// PIXEL SPRITE (canvas-based renderer)
// ═══════════════════════════════════════════════════════════════

interface PixelSpriteProps {
  pixels: Frame;
  palette: Palette;
  scale?: number;
  style?: React.CSSProperties;
}

function PixelSprite({ pixels, palette, scale = 4, style = {} }: PixelSpriteProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    ctx.imageSmoothingEnabled = false;
    const h = pixels.length;
    const w = pixels[0]?.length ?? 0;
    canvas.width = w * scale;
    canvas.height = h * scale;
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    for (let y = 0; y < h; y++) {
      for (let x = 0; x < w; x++) {
        const idx = pixels[y][x];
        if (idx === 0) continue;
        ctx.fillStyle = palette[idx] ?? "transparent";
        ctx.fillRect(x * scale, y * scale, scale, scale);
      }
    }
  }, [pixels, palette, scale]);

  return <canvas ref={canvasRef} style={{ imageRendering: "pixelated", ...style }} />;
}

// ═══════════════════════════════════════════════════════════════
// ANIMATED SPRITE
// ═══════════════════════════════════════════════════════════════

interface AnimatedSpriteProps {
  frames: Frame[];
  palette: Palette;
  scale?: number;
  fps?: number;
  style?: React.CSSProperties;
}

export function AnimatedSprite({ frames, palette, scale = 4, fps = 4, style = {} }: AnimatedSpriteProps) {
  const [frame, setFrame] = useState(0);

  useEffect(() => {
    if (frames.length <= 1) return;
    const interval = setInterval(() => {
      setFrame(f => (f + 1) % frames.length);
    }, 1000 / fps);
    return () => clearInterval(interval);
  }, [frames.length, fps]);

  const currentFrame = frames[frame] ?? frames[0];
  return <PixelSprite pixels={currentFrame} palette={palette} scale={scale} style={style} />;
}

