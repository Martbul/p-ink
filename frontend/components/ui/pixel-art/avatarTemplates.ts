// ─── Avatar Templates ─────────────────────────────────────────────────────────
// pixels: flat array, palette index per pixel, -1 = transparent
// palette: hex colour strings, indexed by pixels array values

export interface AvatarTemplate {
  id: string;
  name: string;
  emoji: string;
  category: string;
  pixels: number[];
  palette: string[];
  width: 16 | 32;
  height: 16 | 32;
}

// Visual ASCII grid parser for creating pixel arrays easily.
// "." = transparent (-1), "0"-"9" and "a"-"z" = palette indices.
function p(strings: TemplateStringsArray): number[] {
  return strings[0]
    .replace(/\s+/g, "")
    .split("")
    .map((c) => (c === "." ? -1 : parseInt(c, 36)));
}

