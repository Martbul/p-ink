// 32 × 32 TEMPLATES
// ─────────────────────────────────────────────────────────────────────────────

// Utility: upscale a 16×16 template to 32×32 (each pixel → 2×2 block)
function upscale(t: AvatarTemplate): AvatarTemplate {
  const arr = new Array(32 * 32).fill(-1);
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const v = t.pixels[y * 16 + x];
      arr[y * 2 * 32 + x * 2] = v;
      arr[y * 2 * 32 + (x * 2 + 1)] = v;
      arr[(y * 2 + 1) * 32 + x * 2] = v;
      arr[(y * 2 + 1) * 32 + (x * 2 + 1)] = v;
    }
  }
  return {
    ...t,
    id: t.id.replace("16-", "32-"),
    width: 32,
    height: 32,
    pixels: arr,
  };
}

